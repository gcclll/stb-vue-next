    let i = 0
    const l2 = c2.length
    let e1 = c1.length - 1 // ‰∏ä‰∏Ä‰∏™ÁªìÊùüÁ¥¢Âºï
    let e2 = l2 - 1 // ‰∏ã‰∏Ä‰∏™ÁªìÊùüÁ¥¢Âºï

